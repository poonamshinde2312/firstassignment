/*Write a program to display ASCII values for character
constants ‘\n’, ‘\r’, ‘\t’, ‘\a’, ‘\b’.
*/
#include<stdio.h>
  
  int main()

  {
  
  printf("ASCII VALUE OF  \\n=%d\n",'\n');
 
  printf("ASCII VALUE OF  \\r=%d\n",'\r');
 
  printf("ASCII VALUE OF \\t=%d\n",'\t');
 
  printf("ASCII VALUE OF \\a=%d\n",'\a');
 
  printf("ASCII VALUE OF \\b=%d\n",'\b');
 
  return 0;
  }
                              
